declare(strict_types=1);

namespace Plumrocket\Datagenerator\Model\Feed;

use Magento\Framework\ObjectManagerInterface;

/**
 * @since 2.3.0
 */
class TagFactory
{
    /**
     * @var \Magento\Framework\ObjectManagerInterface
     */
    private $objectManager;

    /**
     * @param \Magento\Framework\ObjectManagerInterface $objectManager
     */
    public function __construct(ObjectManagerInterface $objectManager)
    {
        $this->objectManager = $objectManager;
    }

    /**
     * @param array $tagData
     * @return \Plumrocket\Datagenerator\Model\Feed\TagInterface
     */
    public function create(array $tagData): TagInterface
    {
        return $this->objectManager->create(TagInterface::class, $tagData);
    }
}
